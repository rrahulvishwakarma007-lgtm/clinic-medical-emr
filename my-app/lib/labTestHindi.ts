export function getLabTestHindi(testName: string): string {
  if (!testName) return "";
  const name = testName.trim();
  if (LAB_TEST_HINDI[name]) return LAB_TEST_HINDI[name];
  // Partial match
  for (const key of Object.keys(LAB_TEST_HINDI)) {
    if (name.toLowerCase().includes(key.toLowerCase()) || key.toLowerCase().includes(name.toLowerCase())) {
      return LAB_TEST_HINDI[key];
    }
  }
  return "";
}