// Matches full medicine name (e.g. "Amoxicillin 500mg") to its Hindi purpose
// by checking if the name starts with any known base name
export function getMedicineHindi(medicineName: string): string {
  if (!medicineName) return "";
  const name = medicineName.trim();

  // 1. Exact match first
  if (MEDICINE_HINDI[name]) return MEDICINE_HINDI[name];

  // 2. Check if the medicine name starts with a known base name
  // Sort by length descending to match longest first (e.g. "Amoxicillin-Clavulanate" before "Amoxicillin")
  const sortedKeys = Object.keys(MEDICINE_HINDI).sort((a, b) => b.length - a.length);
  for (const key of sortedKeys) {
    if (name.toLowerCase().startsWith(key.toLowerCase())) {
      return MEDICINE_HINDI[key];
    }
  }

  return "";
}